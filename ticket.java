
public class ticket {
    protected int standard;
    protected int oap;
    protected int student;
    protected int child;

    public ticket(){
        standard = 0;
        oap = 0;
        student = 0;
        child = 0;
    }

    public int getStandard() {
        return standard;
    }
    public int getOAP() {
        return oap;
    }
    public int getStudent() {
        return student;
    }
    public int getChild() {
        return child;
    }

}
